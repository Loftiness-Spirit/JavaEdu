package Practice3.MathRand.Task2;

public class Point {
    protected int x;
    protected int y;

    public Point(int a, int b){
        x=a;
        y=b;
    }
    public Point(){
    }
    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}